Mengatur Pola Makan/Diet<br>
Anda harus mengatur pola makan sehat dengan asupan jumlah kalori dan nutrisi yang benar dan tepat, dan bukan mengurangi jumlah makanan yang akan membuat tubuh kita menjadi lemas, berkurangnya kekebalan tubuh dan kehilangan mood.<br>
Diet yang benar untuk menurunkan prosentase lemak tubuh adalah dengan makan makanan sehat 5-6 kali sehari dengan porsi kecil-sedang (stop makan sebelum rasa kenyang tercapai).<br>

Pola diet yang benar justru mengharuskan orang sering makan, yaitu dengan membagi porsi makanan dengan jumlah kalori sekian ribu per hari yang biasa dimakan seharian menjadi 5-6 kali makan.<br> Jika jumlah makanan justru dikurangi, maka tubuh sedikit demi sedikit akan menjadi lemas dan kehilangan daya tahan dan kebugaran. Jadi yang benar itu mengurangi jumlah KALORI makanan, bukan mengurangi JUMLAH/VOLUME makanan.<br> Kita tetap bisa makan sering, tapi ganti menunya terutama yang berasal dari sumber karbohidrat.<br>

Pilih dan konsumsi menu yang sehat, berserat tinggi, mengenyangkan, mempercepat metabolisme tubuh dan sekaligus yang mengurangi lemak tubuh. Perbandingan komposisi makanan yang ideal dan sehat antara protein, karbohidrat dan lemak bagi yang ingin menurunkan kadar lemak tubuh dan menggantinya dengan massa otot yang berkualitas adalah 50% : 40% : 10%. Karbohidrat yang dikonsumsi adalah yg memiliki nilai glikemik 'rendah - sedang' contohnya nasi merah, roti gandum, pasta, oatmeal, ubi; sayur2-an terutama brokoli, wortel, buncis, taoge dan sayur2-an hijau seperti sawi, bayam, kangkung; buah2-an dengan rasa manis sedang dan berserat tinggi seperti apel, belimbing dan pepaya.<br>

Konsumsilah protein tinggi dari misalnya daging yang rendah lemak (lean meat), dada ayam, ikan tuna, salmon, dsb. Jadi sebisa mungkin jangan makan nasi putih. Stop (atau minimal kurangi sebanyak mungkin) gula, gorengan, makanan berlemak jahat (saturated fat) dan makanan2 yang terlalu manis, asin dan gurih. Gunakan gula diet secukupnya apabila memang masih membutuhkan gula. Minumlah susu non fat secara teratur, bukan susu rendah lemak. Kalsium 1 gram per hari dari susu non fat akan membantu mengurangi prosentase lemak anda setelah rutin dikonsumsi selama beberapa bulan.<br>

Sebelum mengganti makanan anda dengan makanan yang sehat, makanlah seperti biasa dan hitunglah berapa kalori yang anda habiskan dalam sehari, kemudian timbanglah badan anda.<br>
Kurangi sekitar 250-500 kalori per hari dan makanlah jenis2 makanan sehat seperti di atas dan variannya. Bagi porsinya menjadi 5-6 kali makan, karena tiap kali anda makan, tubuh akan membakar kalori untuk mencernakan makanan yang anda makan, jadi metabolisme anda akan tetap terjaga dengan baik sepanjang hari.<br>

