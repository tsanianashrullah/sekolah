1.Mengkonsumsi karbohidrat sederhana<br>
Untuk melaksanakan program diet, mungkin anda memutuskan untuk mengonsumsi makanan berslogan rendah lemak atau bahkan bebas lemak. Namun, perlu anda ketahui bahwa makanan seperti itu hanyalah menggantikan lemak dengan karbohidrat sederhana. Karbohidrat jenis ini dicerna dengan cepat dalam tubuh. Sehingga anda tidak akan mudah merasa kenyang dan akhirnya mengonsumsi makanan tersebut secara berlebihan. Alih-alih memakan makanan dengan slogan rendah lemak sebaiknya anda mulai mengonsumsi karbohidrat kompleks untuk menunjang program diet anda.<br>

2. Tidak mencari ahli diet dalam membimbing program diet anda<br>
Seorang ahli gizi akan membimbing anda dengan benar bagaimana untuk mengontrol diet anda. Dia akan membantu anda untuk makan secara lebih cerdas. Memang benar bahwa pergi berkonsultasi ke ahli gizi akan mengeluarkan sejumlah uang, namun saran yang anda peroleh dari mereka masih jauh lebih berharga karena bisa dipergunakan seumur hidup anda. Dan tentunya akan lebih bisa membantu dalam melaksanakan sebuah program diet yang jauh lebih efektif daripada mendengarkan saran dari orang lain yang tidak secara khusus ahli dalam bidang tersebut.<br>

3. Kurang tidur pada malam hari<br>
Peneliti dari Wake Forest menyatakan bahwa pelaku diet yang tidur selama sekitar lima jam atau kurang lebih mudah mendapatkan lemak di perutnya. Tidur terlalu kurang dan berlebih sama-sama berakibat tidak baik. Usahakan untuk tidur selama 6 atau 7 jam setiap malam.
<br>
4. Terbiasa meminum soda<br>
Dalam sebuah studi ditemukan bahwa minum satu atau dua kaleng soda setiap hari meningkatkan kesempatan anda memiliki kelebihan berat badan atau obesitas sebesar 33%. Bagi yang memiliki tubuh kurus mungkin ini bukan masalah bagi anda, akan tetapi bagi anda yang saat ini memiliki berat badan berlebih sebaiknya segera eliminasi minuman manis dan soda dalam menu harian anda. Hal ini berdasarkan suatu penelitian, “Target tunggal paling efektif dalam melakukan intervensi yang bertujuan mengurangi obesitas adalah melalui minuman manis seperti soda,” ujar Dr David Ludwig, direktur New Balance Foundation Obesity Prevention Center di Boston Children’s Hospital, seperti dikutip dari Medindia, Kamis (4/10/2012).
<br>
5. Memakan makanan secara terburu-buru
