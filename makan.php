Sup memang bukan makanan yang buruk dan tidak layak dibandingkan dengan makanan-makanan di atas. Namun jika sudah berbicara mengenai garam dan makanan kemasan, maka tentu saja sup kaleng masuk daftar. Akan jauh lebih baik bila Anda memasak sup sendiri. Bukan kalengan.<br>

