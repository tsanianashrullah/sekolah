Pada dasarnya, semua gorengan buruk bagi kesehatan. Jadi jika kentang goreng dilapisi keju, tentu makanan ini masuk dalam daftar paling buruk untuk dikonsumsi. Keju biasanya mengandung lemak 10 kali lebih bayak dari ikan dan daging putih. Apalagi ditambah karbohidrat goreng.<br>


1. Minuman bersoda<br>

Minuman ringan dan soda buruk bagi kesehatan karena mengandung banyak kalori, meskipun dikonsumsi dalam jumlah kecil. Berdasarkan hasil studi, minuman bersoda dapat meningkatkan risiko kerapuhan tulang alias osteoporosis, sakit gigi, dan serangan jantung. Selain itu, minuman-minuman diet juga tidak direkomendasikan karena berpotensi meningkatkan risiko erosi gigi (karena gelembung-gelembung dalam minuman itu bersifat masam).<br>


2. Alcopop berwarna<br>



Alcopop adalah minuman yang mengandung alkohol dengan kadar 4-7 persen. Alcopop mengandung kalori yang tinggi, karena mengandung gula dan alkohol berkalori. Ditambah lagi, minuman itu penuh pewarna dan perasa, yang membuatnya menjadi minuman yang cukup beracun. Panduan yang cukup praktis adalah, semakin cerah warna alcopop, maka semakin buruklah minuman itu untuk dikonsumsi.<br>


3. Makanan cair<br>



Oke, makanan cair memang tidak selalu buruk, namun makanan cair atau minuman makanan pengganti dapat menjauhkan Anda dari mengonsumsi makanan yang tepat. Makanan pengganti mungkin akan lebih cocok dikonsumsi orang yang sedang sakit, namun jangan biarkan makanan semacam itu menggantikan makanan alami.<br>


6. Daging olahan<br>


Daging olahan punya nama lain “daging misteri”, karena tidak jelas apa yang terkandung di dalamnya. Namun satu hal yang pasti, jika daging olahan itu dikemas dalam kaleng dan jenis dagingnya tidak jelas, maka daging tersebut tidak baik bagi tubuh. Berusahalah untuk menghindari sosis dan salami, karena hasil olahan dengan lemak dan garam.<br>


7. Chicken nugget<br>



Chicken nugget hampir sama dengan sosis, yakni berasal dari daging sisa dicampur dengan tepung. Dan saat nugget-nugget kecil itu digoreng, maka akan meningkatkan levelnya sebagai makanan terburuk untuk dikonsumsi. Sepotong kecil nugget yang digoreng menyerap lebih banyak lemak dari hasil penggorengan itu.<br>


8. Donat<br>



Jika ada satu makanan yang melambangkan makanan junk food abad ke-21, itu adalah donat. Baik dilapisi, diisi, dikilapkan dengan gula dan selai, atau yang polos sekalipun, makanan ini tidak baik bagi tubuh. Bukan hanya sekedar masalah tepung halus dan gula halus, namun makanan ini juga digoreng dalam minyak sulingan. Donat akan mengganggu keseimbangan gula darah Anda, dan dapat mempercepat proses pembakaran, sehingga Anda akan cepat merasa lapar lagi.<br>


9. Sup Kaleng<br>

